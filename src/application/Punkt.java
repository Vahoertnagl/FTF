package application;

public class Punkt {
	private int x;
	private int y;
	
	
	public Punkt(int x, int y) {
		super();
		this.x = x + 450;
		this.y = y + 50;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
	
}
